package org.codice.ditto.replication.admin.test;

import java.util.UUID;
import org.codice.ditto.replication.api.data.ReplicationSite;

public class ReplicationSiteImpl implements ReplicationSite {

  private String id;

  private String name;

  private String url;

  public ReplicationSiteImpl() {}

  public ReplicationSiteImpl(String name, String url) {
    this.id = UUID.randomUUID().toString();
    this.name = name;
    this.url = url;
  }

  @Override
  public String getId() {
    return id;
  }

  @Override
  public void setId(String id) {
    this.id = id;
  }

  @Override
  public int getVersion() {
    return 1;
  } // the api isn't concerned with the version

  @Override
  public String getName() {
    return name;
  }

  @Override
  public void setName(String name) {
    this.name = name;
  }

  @Override
  public String getUrl() {
    return url;
  }

  @Override
  public void setUrl(String url) {
    this.url = url;
  }
}
